export const meta = {
  name: 'verify-length-control-causal',
  description: 'Adversarially verify the causal length-control verdict on deception_v0 (skeptics by failure-mode lens + completeness critic + synthesis)',
  phases: [
    { title: 'Refute' },
    { title: 'Critique' },
    { title: 'Synthesize' },
  ],
}

// args = { resultPath, preregPath, scriptPath, findingPath, summary }
const A = args || {}
const CTX = `You are adversarially reviewing a styxx research finding. The program's thesis is that the
discipline (self-falsification) IS the product, so your job is to REFUTE, not to praise. Default to
"the verdict does NOT survive" unless the artifacts force otherwise.

Read these files before judging (use the Read tool):
- Pre-registration (frozen thresholds, BEFORE data): ${A.preregPath}
- Result JSON (mechanical verdict + per-feature diagnostics + CIs): ${A.resultPath}
- The analysis harness (how AUC/manip-check/thresholds are computed): ${A.scriptPath}
${A.findingPath ? `- The draft finding: ${A.findingPath}` : ''}

One-line summary of what was claimed: ${A.summary || '(see result JSON)'}
`

const VSCHEMA = {
  type: 'object',
  additionalProperties: false,
  required: ['lens', 'strongest_objection', 'verdict_survives', 'severity', 'what_would_fix', 'evidence'],
  properties: {
    lens: { type: 'string' },
    strongest_objection: { type: 'string', description: 'the single most damaging concrete objection from this lens' },
    verdict_survives: { type: 'boolean', description: 'does the pre-registered verdict survive THIS objection?' },
    severity: { type: 'string', enum: ['fatal', 'major', 'minor', 'none'] },
    what_would_fix: { type: 'string', description: 'concrete check/caveat that would neutralize the objection' },
    evidence: { type: 'string', description: 'specific numbers/lines from the artifacts that ground the objection' },
  },
}

const LENSES = [
  { key: 'generator-validity', prompt: `LENS: generator mismatch. The matched corpus was generated by a LOCAL 3B instruct model, not gpt-4o-mini (which trained the shipped weights). Attack: could a low/high AUC reflect the 3B model failing to COMMIT to the honest/dishonest personas (weak stance-following) rather than length-control? Is the honest/dishonest contrast in the matched corpus comparable to the original? Use the per-feature std-diffs (e.g. vagueness_density, specificity_density) in BOTH corpora to argue whether the register contrast was preserved or weakened by the generator. Does the cross-generator agreement (Qwen vs Llama) defend against this, or could both 3B models share the same failure?` },
  { key: 'construct-destroying', prompt: `LENS: the test broke the data. Forcing ~55 words may itself destroy the construct: if honest specificity REQUIRES length (you can't be specific in 12 words), equalizing length removes the honest answer's capacity to be specific, so a low AUC means "the test is unfair", not "the instrument is a length detector". Decide from the per-feature std-diffs: did the NON-length register features (specificity_density, vagueness_density, hedge_confidence_clash) SURVIVE at matched length or collapse with it? If they survived, this objection fails; if they collapsed, the verdict's interpretation is confounded. State which.` },
  { key: 'statistical-power', prompt: `LENS: underpowered / overclaim. n=200 per model, single greedy seed. Examine the bootstrap CIs in the result JSON. Do the CIs cleanly separate the verdict band from its neighbors, or do they straddle a frozen threshold (0.62 / 0.75)? Is the cross-generator "agreement" real or within overlapping CIs? Is any headline number stated more confidently than its CI supports?` },
  { key: 'prereg-fidelity', prompt: `LENS: threshold gaming / narrative drift. Compare the verdict the NUMBERS force (apply the frozen thresholds in the prereg to the result JSON yourself) against the verdict/story in the result and draft finding. Did the verdict() function get applied mechanically, or did the narrative reach past what the thresholds permit? Was the manipulation check (|d_len|<=0.30) actually satisfied — if not, is the AUC correctly reported as an upper bound?` },
  { key: 'within-bin-coherence', prompt: `LENS: internal contradiction. The original-corpus within-length-bin shipped AUC (~0.927 in the populated bin) and the matched-length regen AUC are two routes to the same question. Do they AGREE? If the regen says SPURIOUS but the within-bin says ~0.93, that is a contradiction someone will catch — reconcile it or flag it as unresolved. Which route is more trustworthy and why (post-hoc conditioning on thin overlap vs generative control)?` },
]

phase('Refute')
const refutations = await parallel(LENSES.map(L => () =>
  agent(`${CTX}\n\n${L.prompt}\n\nReturn your single strongest objection from this lens as structured output.`,
    { label: `refute:${L.key}`, phase: 'Refute', schema: VSCHEMA })
))

phase('Critique')
const critique = await agent(
  `${CTX}\n\nYou are the COMPLETENESS critic. The refutations so far: ${JSON.stringify(refutations.filter(Boolean).map(r => ({lens:r.lens, obj:r.strongest_objection, sev:r.severity})))}.
What is MISSING from this verification? Consider: an instrument not tested (only deception was run — loop/overconfidence/plan_action are also length-DOMINATED), a model family not run, a confound not checked, a claim in the finding with no backing number, a way the whole framing could be wrong. Return the 3 most important gaps and, for each, whether it BLOCKS publishing the deception verdict or is follow-up work.`,
  { label: 'completeness-critic', phase: 'Critique',
    schema: { type:'object', additionalProperties:false, required:['gaps'], properties:{ gaps:{ type:'array', items:{ type:'object', additionalProperties:false, required:['gap','blocks_publish','why'], properties:{ gap:{type:'string'}, blocks_publish:{type:'boolean'}, why:{type:'string'} } } } } } })

phase('Synthesize')
const synth = await agent(
  `${CTX}\n\nSynthesize the adversarial review into a publish decision.
Refutations: ${JSON.stringify(refutations.filter(Boolean))}
Completeness gaps: ${JSON.stringify(critique?.gaps || [])}

Decide: (1) does the pre-registered deception verdict SURVIVE adversarial review? (2) the exact list of caveats the finding MUST carry to be honest, (3) any number that must be softened, (4) what follow-up is owed but does NOT block. Be specific and concrete; cite the lens for each required caveat.`,
  { label: 'synthesis', phase: 'Synthesize',
    schema: { type:'object', additionalProperties:false, required:['survives','fatal_objections','required_caveats','numbers_to_soften','followup_not_blocking','one_line'],
      properties:{ survives:{type:'boolean'}, fatal_objections:{type:'array', items:{type:'string'}}, required_caveats:{type:'array', items:{type:'string'}}, numbers_to_soften:{type:'array', items:{type:'string'}}, followup_not_blocking:{type:'array', items:{type:'string'}}, one_line:{type:'string'} } } })

return { refutations: refutations.filter(Boolean), completeness: critique?.gaps || [], synthesis: synth }
